package day14.TreeMap;

public class AnsiColor {
    public static final String ANSI_RED = "\u001B[41m"; // подцветка разницы доход - расход - красный фон
    public static final String ANSI_BLACK = "\u001b[30m"; // черный
    public static final String ANSI_SCARLET = "\u001b[31m"; // алый
    public static final String ANSI_GREEN = "\u001b[32m"; // зеленый
    public static final String ANSI_YELLOW = "\u001b[33m"; // Жёлтый
    public static final String ANSI_BLUE = "\u001b[34m"; // Синий
    public static final String PURPLE = "\u001b[35m"; // Пурпурный
    public static final String ANSI_CYAN = "\u001b[36m"; // Голубой
    public static final String ANSI_WHITE = "\u001b[37m"; // Белый
    public static final String ANSI_RESET = "\u001B[0m"; // конец подцветки - черный фон

    public static final String ANSI_CLEAR_TERMINAL = "\033[2J";
}
